import { ComponentProps, splitProps } from 'solid-js';
import { Box, BoxProps, BoxComponent } from '../';

import css from './index.module.css';

export interface FontawesomeRootOptions
{
    icon: keyof ( typeof FontawesomeIcons );
}

export type FontawesomeRootProps<T extends BoxComponent> = BoxProps<T> & FontawesomeRootOptions;

export function FontawesomeRoot<T extends BoxComponent = 'div'> (
    props: FontawesomeRootProps<T> & Omit<ComponentProps<T>, keyof FontawesomeRootProps<T>>
)
{
    const [ local, others ] = splitProps( props, [ 'as', 'icon', 'class' ] );

    return (
        <Box
            as={ String( local[ 'as' ] || 'div' ) }
            class={ [ css[ 'fontawesome--root' ], local[ 'class' ] ].filter( Boolean ).join( ' ' ) }
            style={ {
                'font-family': 'fontawesome'
            } }
            { ...others }
        >
            { FontawesomeIcons[ local[ 'icon' ] ] }
        </Box>
    );
}

